#!/bin/bash

#===============================================================================
# Overview
#===============================================================================
# This script generates Figure 6 and Table 3 from the GRT paper.
# It executes `mutation.sh` multiple times, varying:
#   - Execution time per class (SECONDS_PER_CLASS)
#   - Subject programs (SUBJECT_PROGRAMS)
#   - Feature variants (FEATURES)
#
#===============================================================================
# Output
#===============================================================================
# `results/fig6-table3-info.csv`:   Contains raw data collected from `mutation.sh`. This file is
#                                   appended to by each experimental run.
# `results/fig6-table3-report.pdf`: Final report containing Figure 6 and Table 3,
#                                   generated from `results/info.csv`.
#
#===============================================================================
# Important Notes
#===============================================================================
# Running this script will overwrite any previous reports generated by this script
# (results/fig6-table3-report.pdf and results/fig6-table3-info.csv).
# If you wish to preserve a previous report, **make sure to download or back it up before running
# this script again**.
#
#------------------------------------------------------------------------------
# Usage (run in experiment-scripts/ directory):
#------------------------------------------------------------------------------
#   ./mutation-fig6-table3.sh
#------------------------------------------------------------------------------
# Prerequisites:
#------------------------------------------------------------------------------
# See file `mutation-prerequisites.md`.
#
#===============================================================================

SCRIPTDIR="$(cd "$(dirname "$0")" > /dev/null 2>&1 && pwd -P)"
cd "$SCRIPTDIR" || exit 2
MUTATION_DIR="$(realpath ../)"

PYTHON_EXECUTABLE=$(command -v python3 2> /dev/null || command -v python 2> /dev/null)
if [ -z "$PYTHON_EXECUTABLE" ]; then
  echo "Error: Python is not installed." >&2
  exit 1
fi

pip install pandas
pip install matplotlib
pip install seaborn

# Clean up previous run artifacts
rm -rf "$MUTATION_DIR"/build/bin/*
rm -rf "$MUTATION_DIR"/build/test/*
rm -rf "$MUTATION_DIR"/build/lib/*
rm -f "$MUTATION_DIR"/results/fig6-table3-report.pdf
rm -f "$MUTATION_DIR"/results/fig6-table3-info.csv

#===============================================================================
# The GRT paper's parameters are as follows:
NUM_LOOP=10
SECONDS_PER_CLASS=(2 10 30 60)
SUBJECT_PROGRAMS=(
  "a4j-1.0b"
  "asm-5.0.1"
  "bcel-5.2"
  "ClassViewer-5.0.5b"
  "commons-cli-1.2"
  "commons-codec-1.9"
  "commons-collections4-4.0"
  "commons-compress-1.8"
  "commons-lang3-3.0"
  "commons-math3-3.2"
  "commons-primitives-1.0"
  "dcParseArgs-10.2008"
  "easymock-3.2"
  "fixsuite-r48"
  "guava-16.0.1"
  "hamcrest-core-1.3"
  "javassist-3.19"
  "javax.mail-1.5.1"
  "jaxen-1.1.6"
  "jcommander-1.35"
  "jdom-1.0"
  "joda-time-2.3"
  "JSAP-2.1"
  "jvc-1.1"
  "nekomud-r16"
  "pmd-core-5.2.2"
  "sat4j-core-2.3.5"
  "shiro-core-1.2.3"
  "slf4j-api-1.7.12"
  "tiny-sql-2.26"
)
FEATURES=(BASELINE GRT EVOSUITE)

# Temporary parameters for testing that override the defaults, since we haven't
# implemented all GRT features. See mutation.sh for a list of different features
# you can specify.
NUM_LOOP=1
SECONDS_PER_CLASS=(2)
SUBJECT_PROGRAMS=(
  "dcParseArgs-10.2008"
  "slf4j-api-1.7.12"
)
FEATURES=(
  "BASELINE"
  "BLOODHOUND"
)

NUM_CORES=$(($(nproc) - 4))
echo "Running $NUM_CORES concurrent processes"

#===============================================================================
# Task Generation & Execution
#===============================================================================
TASKS=()
for cseconds in "${SECONDS_PER_CLASS[@]}"; do
  for program in "${SUBJECT_PROGRAMS[@]}"; do
    for feature in "${FEATURES[@]}"; do
      for i in $(seq 1 "$NUM_LOOP"); do
        TASKS+=("$MUTATION_DIR $cseconds $program $feature")
      done
    done
  done
done

# Export function for parallel execution
# Each run's output is redirected to mutation_output.txt within its respective results directory.
run_task() {
  mutation_dir=$1
  cseconds=$2
  program=$3
  feature=$4
  echo "Running: (cd $mutation_dir && ./mutation.sh -c $cseconds -f $feature -r -i fig6-table3 $program)"
  (cd "$mutation_dir" && ./mutation.sh -c "$cseconds" -f "$feature" -r -i "fig6-table3" "$program")
}

export -f run_task

# Run tasks in parallel.
printf "%s\n" "${TASKS[@]}" | parallel -j $NUM_CORES --colsep ' ' run_task

#===============================================================================
# Figure Generation (Table III and Fig. 6)
#===============================================================================

# Outputs figures to result/fig6-table3-report.pdf
"$PYTHON_EXECUTABLE" "$MUTATION_DIR"/experiment-scripts/generate-grt-figures.py fig6-table3
